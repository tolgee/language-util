const result = {} as { [key: string]: LanguageInfo }
availableLanguages.availableLocales.modern.forEach(l => {
    if (l !== "root") {
        const enDn = new (Intl as any).DisplayNames("en", {type: "language", style: "long"})
        const orDn = new (Intl as any).DisplayNames(l, {type: "language", style: "long"})
        result[l] = {
            englishName: enDn.of(l),
            originalName: orDn.of(l)
        }
    }
})

